#include <Workflow/TexCompression.h>
#include <Packing/TexCompressionOnTheFly.h>
#include <Images/Image.h>

namespace Packing
{
//-----------------------------------------------------------------------------
Ptr<Assets::Texture> OnTheFlyTextureGenerator::generateTexture(const String & name)
{
    Ptr<Images::Image> pImage(new Images::Image());
    pImage->loadFile(name);

    Workflow::TextureOptions options;

    if(name.find(L"-normal.") != String::npos)
        options.texType = Workflow::TEXTURE_NORMALMAP;

    return Workflow::generateTexture(options, pImage);
}
//-----------------------------------------------------------------------------
}
